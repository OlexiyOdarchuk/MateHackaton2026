package ai

import (
	"log/slog"
	"SuperAdds/internal/scraper"
)

// SummarizeAds мокова функція, яка типу робить вижимку з 5 найкращих реклам.
// В реальності тут би був запит до Vision AI (наприклад GPT-4V), щоб описати фото та тексти.
func SummarizeAds(creatives []scraper.Creative) (string, error) {
	slog.Info("Мок: робимо вижимку з реклам", "count", len(creatives))
	
	summary := "Аналіз 5 найкращих реклам показав наступне: " +
		"Найчастіше використовується яскравий жовтий колір та емоційні заклики до дії. " +
		"Головний посил: 'Отримай знижку сьогодні!'. " +
		"Люди на фотографіях виглядають щасливими та здивованими. " +
		"Основний акцент робиться на швидкості доставки."
		
	return summary, nil
}
